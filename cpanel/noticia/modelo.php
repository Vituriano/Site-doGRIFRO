	<p class="subtitulo"><b>O Instituto Federal de Educação, Ciência e Tecnologia de Rondônia (IFRO), através da Pró-Reitoria de Pesquisa, Inovação e Pós-Graduação (Propesp), incentiva seus servidores a submeterem “Relatos de experiências integradoras exitosas” em Chamada Pública lançada pelo Instituto Federal de Brasília (IFB).</b></p>
	<center><img style='width: 80%; height: auto;' src='https://www.grupoescolar.com/a/b/artigo-1F.jpg'></center>

	<p class='paragrafo'>Segundo o Edital, serão selecionadas dez experiências integradoras exitosas, sendo duas para cada uma das cinco linhas temáticas: 1) Integração da comunidade mediante a inclusão de minorias em espaços educativos (pessoas com deficiência, questões de gênero, vulnerabilidade social, raça e diversidade sexual); 2) Integração entre componentes curriculares na perspectiva de uma educação profissional articulada com o mundo do trabalho; 3) Integração entre saberes da educação profissional com saberes da educação básica; 4) Integração entre ensino, pesquisa e extensão; 5) Integração de sucesso entre a Escola e outros setores da sociedade (empresas, instituições, ongs, associações) numa proposta de extensão tecnológica ou pesquisa aplicada.<br><br>

		Os dez trabalhos escolhidos farão parte do “Caderno de Relatos de Experiências Integradoras Exitosas – 2019” (formato eletrônico).  Os autores das propostas selecionadas poderão apresentar seus Relatos em evento a ser promovido pelas Pró-Reitorias de Ensino (PREN), Extensão e Cultura (PREX), e Pesquisa e Inovação (PRPI), na semana de 26 a 30 de agosto de 2019, no âmbito do ConectaIF 2019, em Brasília (DF).<br><br>

		Os requisitos para participar, bem como formulários e anexos estão disponíveis na Chamada Pública, que pode ser conferida AQUI. As inscrições devem ser encaminhadas para o e-mail relatos@ifb.edu.br, até o dia 16 de abril de 2019.<br><br>

		O Pró-Reitor de Pesquisa, Inovação e Pós-Graduação, Gilmar Alves Lima Júnior, explica que em 2019, em paralelo a realização do CONECTA IF, organizado pelo IFB, a Rede Federal organizará um evento nacional com programação dedicada a discutir o ensino, pesquisa e extensão. “Aproveitando a publicação do Edital para selecionar experiências exitosas a serem apresentadas no evento do IFB e considerando as ações desenvolvidas no âmbito do IFRO e de resultados relevantes até nacionalmente, convidamos a comunidade a enviar propostas e informamos que em breve, a Reitoria, em conjunto com os Campi, irá divulgar o fomento a participação, através de editais. A divulgação do que executamos na nossa Instituição e a troca de experiência, são primordiais a cada dia”, destaca.<br><br>

